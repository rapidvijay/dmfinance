using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using ModificationHousingFeesAdmin.Models;

namespace ModificationHousingFeesAdmin.Controllers
{
    [ValidateInput(true)]
    public class UsersController : Controller
    {
        private ModificationHousingFeesEntities db = new ModificationHousingFeesEntities();

        // GET: Users
        public ActionResult Index()
        {
            return View(db.Users.ToList());
        }

        // GET: Users/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            User user = db.Users.Find(id);
            if (user == null)
            {
                return HttpNotFound();
            }
            return View(user);
        }

        // GET: Users/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Users/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        //[ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "id,USER_NAME,PASSWORD,NAME_ARABIC,NAME_ENGLISH,EMAIL,Has_Review_Role,Has_Manul_Update_Role_In_Dewa,Created_By,Creation_Date,Updated_By,Update_Date,Has_Admin_Role")] User user)
        {
            if (ModelState.IsValid)
            {
                //##########################################################################
                user.Created_By = Common.User.USER_NAME;
                user.Creation_Date = DateTime.Now;
                //##########################################################################
                db.Users.Add(user);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            return View(user);
        }

        // GET: Users/Edit/5
        public ActionResult Edit(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            User user = db.Users.Find(id);
            if (user == null)
            {
                return HttpNotFound();
            }
            return View(user);
        }

        // POST: Users/Edit/5
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        //[ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "id,USER_NAME,PASSWORD,NAME_ARABIC,NAME_ENGLISH,EMAIL,Has_Review_Role,Has_Manul_Update_Role_In_Dewa,Created_By,Creation_Date,Updated_By,Update_Date,Has_Admin_Role")] User user)
        {
            if (ModelState.IsValid)
            {
                //#####################################################################
                user.Updated_By = Common.User.USER_NAME;
                user.Update_Date = DateTime.Now;
                //#####################################################################
                db.Entry(user).State = EntityState.Modified;
                db.SaveChanges();
                return RedirectToAction("Index");
            }
            return View(user);
        }

        // GET: Users/Delete/5
        public ActionResult Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            User user = db.Users.Find(id);
            if (user == null)
            {
                return HttpNotFound();
            }
            return View(user);
        }

        // POST: Users/Delete/5
        [HttpPost, ActionName("Delete")]
        //[ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            User user = db.Users.Find(id);
            db.Users.Remove(user);
            db.SaveChanges();
            return RedirectToAction("Index");
        }

        protected override void Dispose(bool disposing)
        {
            if (disposing)
            {
                db.Dispose();
            }
            base.Dispose(disposing);
        }
    }
}
